function enableButtons(){
    buttonScale.disabled = false;
    buttonUnscale.disabled = false;
    buttonSwapPoints.disabled = false;
    buttonFindPath.disabled = false;
    buttonClearPoints.disabled = false;
    buttonFindNodes.disabled = false;
    city_chooser.disabled = false;
}

function disableButtons() {
    buttonScale.disabled = true;
    buttonUnscale.disabled = true;
    buttonSwapPoints.disabled = true;
    buttonFindPath.disabled = true;
    buttonClearPoints.disabled = true;
    buttonFindNodes.disabled = true;
    city_chooser.disabled = true;
}